unsigned long	ft_uatoi(const char *nptr)
{
	int	num;

	num = 0;
	while (*nptr == ' ')
		nptr++;
	if (*nptr == '+')
		nptr++;
	while ((*nptr >= '0') && (*nptr <= '9'))
	{
		num = num * 10 + (*nptr - '0');
		nptr++;
	}
	return (num);
}
